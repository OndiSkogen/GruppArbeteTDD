        public void PlayerMove(Point old, Point move)
        {
            if (OnCollision(move))
            {
                gameBoard[move.X, move.Y] = 2;
            }            
        }

        public void RemoveOldPlayer(Point p)
        {
            gameBoard[p.X, p.Y] = 0;
        }

        public bool OnCollision(Point p)
        {
            if (gameBoard[p.X, p.Y] == 1) return false;
            else return true;
        }

